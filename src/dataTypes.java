
public class dataTypes {
	
	public static void main(String[] args) {
		/*
		 * every variables should be declared by data types  - put evrything in the right size 
		 * primitive data types
		 * integer : byte , short , int, long
		 * decimal : float , double
		 * character : char
		 * boolean :true/false
		 * 
		 * non primitive data types
		 * string
		 */
		
		//syso+cntrl+space for shortcut of print
		byte id = -128;
		System.out.println(id);
		
		short s1 = 32567;
		System.out.println(s1);
		
		int i = 1234555577;
		System.out.println(i);
		
		long l = 1222333456067L;
		System.out.println(l);
		
		
		float f = 65.28906656f;
		System.out.println (f); 
		
		double d = 1677.444455488888888;
		System.out.println (d);
		
		char c = '$'; //$.%,&
		System.out.println(c);
		
		boolean x = true;
		boolean y = false;
		
		System.out.println(x);
		System.out.println(y);
		
		String x1 = "we are learnign java";
		System.out.println(x1);
		
		boolean bb1 = true;
		boolean bb2 = false;
		
		//boolean bb3 = True;  doesn't support
		//boolean bb3 = False; doesn't support
		
		//boolean bb5=1; doesn't support
		//boolean bb6=0; doesn't support
		
		
	}

}
